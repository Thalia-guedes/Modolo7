package Exercicio3

/*
Escreva um algoritmo que exiba 20 vezes a mensagem “Eu gosto de
estudar Algoritmos!”. Utilize o comando de repetição Enquanto
(While)
 */

fun main (){
    var mensagem = 0

    while (mensagem < 20){
        println("Eu gosto de estudar Algoritimos! ")
        mensagem++
    }

}